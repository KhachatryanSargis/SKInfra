/// SKCore — Reusable iOS infrastructure library.
///
/// Provides type-safe protocols and utilities for:
/// - **DI** — Framework-agnostic dependency injection container protocol
/// - **Storage** — Generic, key-typed persistence (UserDefaults, Keychain)
/// - **Logger** — Protocol-oriented logging (Print, OSLog, Composite)
/// - **Namespace** — Struct-based extension namespace (`.sk.`)
/// - **Extensions** — Namespaced String, Optional, Collection utilities
///
/// Designed for dependency injection, full testability, and zero
/// third-party dependencies.
